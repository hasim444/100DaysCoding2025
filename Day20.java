class Main {
    public static void main(String[] args) {

        // int → String
        int angka = 123;
        String angka1 = Integer.toString(angka);
        System.out.println("Ttipe data int ke String   : " + angka1);
      // double → String
        double angka2 = 170.5;
        String angka3 = Double.toString(angka2);
        System.out.println("Tipe data double ke String : " + angka3);
    }
}

